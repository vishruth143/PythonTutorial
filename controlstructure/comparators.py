"""
== -> Value Equality
!= -> Not equal to
< -> Less than
> -> Greater than
<= -> Less than or equal to
>= -> Greater than or equal to
"""

bool_one = 10 == 11
not_equal = 10 != 11
less_than = 10 < 100
greater_than = 1 > 100
lt_eq = 10 <= 10
gt_eq = 100 >= 90 + 10

print(bool_one)
print(not_equal)
print(less_than)
print(greater_than)
print(lt_eq)
print(gt_eq)
